package entidades;

public class Pais {
 private int id;
 private String nombre;
 private String continente;
 private String tipoRegion;
 private String coddigoAlfa2;
 private String codigoAlfa3;
 
 public int getId() {
    return id;
 }
 public void setId(int id) {
    this.id = id;
 }
 public String getNombre() {
    return nombre;
 }
 public void setNombre(String nombre) {
    this.nombre = nombre;
 }
 public String getContinente() {
    return continente;
 }
 public void setContinente(String continente) {
    this.continente = continente;
 }
 public String getTipoRegion() {
    return tipoRegion;
 }
 public void setTipoRegion(String tipoRegion) {
    this.tipoRegion = tipoRegion;
 }
 public String getCoddigoAlfa2() {
    return coddigoAlfa2;
 }
 public void setCoddigoAlfa2(String coddigoAlfa2) {
    this.coddigoAlfa2 = coddigoAlfa2;
 }
 public String getCodigoAlfa3() {
    return codigoAlfa3;
 }
 public void setCodigoAlfa3(String codigoAlfa3) {
    this.codigoAlfa3 = codigoAlfa3;
 }

 
}
